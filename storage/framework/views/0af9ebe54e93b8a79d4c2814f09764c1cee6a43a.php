    <script>
        let original = window.location.origin
        let name =  document.getElementById("name");
        let email = document.getElementById("email");
        let comments = document.getElementById("comments");
        let submit = document.getElementById("submit");
        let zoex = document.querySelector(".zoex")
        var  token = document.querySelector('meta[name="csrf-token"]').getAttribute('content');
        submit.addEventListener("click", function(e){
            e.preventDefault();
            console.log(name, email, comments);
            let headers = new Headers();
            headers.append('Content-Type', 'application/json')
            let formData = new FormData();
            formData.append('name', name.value)
            formData.append('email', email.value)
            formData.append('comments', comments.value)
            formData.append('_token', token)
            let url = `${original}/contact`;
            axios.post(url, formData, headers).then(res=>{
               if(res.data.success){
                zoex.innerText = res.data.success
                zoex.style.color = "green"

               }

            }).catch(err=>{
                let error = err.response.data.errors
                if(error.email){
                    zoex.innerText =  error.email[0]
                    zoex.style.color = "red"
                }else if(error.name){
                    zoex.innerText =  error.name[0]
                    zoex.style.color = "red"
                }else if(error.comments){
                    zoex.innerText =  error.comments[0]
                    zoex.style.color = "red"
                }
             })
        })


